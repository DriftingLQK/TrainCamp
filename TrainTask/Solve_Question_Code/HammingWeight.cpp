/*****************第8题：***********************************************
[要求]
编写一个函数，输入是一个无符号整数，返回其二进制表达式中数字位数为‘1’的个数（也被称为汉明重量）。
***********************************************************************/

int HammingWeight(unsigned int n) {
	int count = 0;	//计算二进制中1的个数
	while (n != 0)  //遍历
	{
		if (n % 2 == 1) //满足尾数是1.
			count++;
		n /= 2;          //除2，右移一位。(二进制)  n = n >> 1;
	}
	return count;
}
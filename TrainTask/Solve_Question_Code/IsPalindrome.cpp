/***************************第四题：***************************
[要求]
判断一个整数是否是回文数。回文数是指正序（从左向右）和倒序（从右向左）读都是一样的整数。

***********************************************/////
/*随意找一个十进制的数，把它倒过来成另一个数，比较两个数是否相等*/
bool IsPalindrome(int x)
{
	if (x<0)         //如果输入为负数，则默认不是回文数
	{
		return false;
	}
	long temp = x, n = 0;
	while (temp)      //   将原数倒序表示
	{
		n = n * 10 + temp % 10;
		temp = temp / 10;
	}
	return (x == n);
}

